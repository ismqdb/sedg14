static void swap(i32 *x, i32 *y){
    i32 z;

    z = *x;
    *x = *y;
    *y = z;
}

/* ******************************************************************************** */

static i32 gcdMod(i32 u, i32 v){
    i32 t;

    u = abs(u);
    v = abs(v);

    while(u>0){
        if(u<v)
            swap(&u, &v);
        u %= v;
    }
    return v;
}

/* ******************************************************************************** */

struct fraction reduce(struct fraction x){
    i32 gcd = gcdMod(x.numerator, x.denominator);

    x.numerator /= gcd;
    x.denominator /= gcd;

    return x;
}

