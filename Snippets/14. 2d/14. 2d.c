i32 twoD(){
    #define size 5
    i32 array[size][size] = {0};

    for(i32 i = 1; i < size; i++)
        for(i32 j = 1; j < size; j++)
            if(gcd(i, j) == 1)
                array[i][j] = 1;
            else
                array[i][j] = 0;

    putchar(10); 
}

