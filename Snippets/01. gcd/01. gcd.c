none swap(u64 *x, u64 *y){
    u64 z;

    z = *x;
    *x = *y;
    *y = z;
}

/* ******************************************************************************** */

u64 gcd(u64 u, u64 v){
    u64 t;

    u = abs(u);
    v = abs(v);

    while(u > 0){
        if(u < v)
            swap(&u, &v);
        u = u-v;
    }
    return v;
}

/* ******************************************************************************** */

u64 gcdRecursive(u64 *u, u64 *v){
    u64 t;

    *u = abs(*u);
    *v = abs(*v);

    if(*u > 0){
        if(*u < *v)
            swap(u, v);
        *u %= *v;
        gcdRecursive(u, v);
    }
    return *v;
}

