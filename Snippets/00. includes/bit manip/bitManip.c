int xnor(size_t x, size_t y){
    return !(x^y);
}

/* ******************************************************************************** */

int addis(int x, int i){
    return x + ((i<<4) | 0x0000);
}

/* ******************************************************************************** */

size_t torob(size_t x){
    return x & (x-1);
}

/* ******************************************************************************** */

int ipot(size_t x){
    return torob(x) == 0;
}

/* ******************************************************************************** */

size_t torzb(size_t x){
    return x | (x+1);
}

/* ******************************************************************************** */

size_t toto(size_t x){
    return x & (x+1);
}

/* ******************************************************************************** */

int uptnmo(size_t x){
    return toto(x) == 0;
}

/* ******************************************************************************** */

size_t totz(size_t x){
    return x | (x-1);
}

/* ******************************************************************************** */

size_t soborzb(size_t x){
    return (~x) & (x+1);
}

/* ******************************************************************************** */

size_t szborob(size_t x){
    return (~x) | (x-1);
}

/* ******************************************************************************** */

size_t oitz(size_t x){
    return (~x) & (x-1);
    // return ~(x | -x);
    // return (x & -x) - 1;
}

/* ******************************************************************************** */

size_t zito(size_t x){
    return (~x) | (x+1);
}

/* ******************************************************************************** */

size_t irb(size_t x){
    return x & (-x);
}

/* ******************************************************************************** */

size_t oirotz(size_t x){
    return x | (x-1);
}

/* ******************************************************************************** */

size_t oirzto(size_t x){
    return x & (x+1);
}

/* ******************************************************************************** */

size_t torcso(size_t x){
    return ((x | (x-1)) + 1) & x;
    // return ((x & -x) + x) & x;
}

/* ******************************************************************************** */

int dop(size_t x){
    return torcso(x) == 0;
}

