#include "./node.h"

/* ******************************************************************************** */

node* nodeAlloc(){
    return (node*)malloc(sizeof(node*));
}

/* ******************************************************************************** */

node* nodeAllocInt(int value){
    node *n = (node*)malloc(sizeof(node*));
    n->key = value;
    return n;
}

/* ******************************************************************************** */

void nodeDealloc(node *node){
    if(node != NULL)
        free(node);
}

