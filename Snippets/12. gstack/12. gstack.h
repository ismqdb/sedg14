struct gstack;

/* ******************************************************************************** */

struct stackVTable {
    void    (*push)     (struct gstack*, const void*);
    void    (*pop)      (struct gstack*);
    void*   (*top)      (struct gstack*);
    void    (*destroy)  (struct gstack*);
};

/* ******************************************************************************** */

struct gstack {
    const struct stackVTable *vtable;
};

